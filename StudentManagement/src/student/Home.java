    public boolean isEmptyStudent() {
        if (jTextField46.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student name is missing");
            return false;
        }
        if (jDateChooser5.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Student date of birth is missing");
            return false;
        }
        if (jDateChooser5.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(this, "No student from the future are allowed");
            return false;
        }
        if (jTextField39.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student E-mail address is missing");
            return false;
        }
        if (!jTextField39.getText().matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Invalied E-mail address");
            return false;
        }
        if (jTextField40.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student phone number is missing");
            return false;
        }
        if (jTextField40.getText().length() >= 15) {
            JOptionPane.showMessageDialog(this, "Phone number is too long");
            return false;
        }
        if (jTextField41.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student father name is missing");
            return false;
        }
        if (jTextField42.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student mother name is missing");
            return false;
        }
        if (jTextField43.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student address line 1 is missing");
            return false;
        }
        if (jTextField44.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student address line 2 is missing");
            return false;
        }
        if (imagePath == null) {
            JOptionPane.showMessageDialog(this, "Please add your image");
            return false;
        }

        return true;
    }

